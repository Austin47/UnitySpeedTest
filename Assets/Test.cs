    [SerializeField] private Text _textPrefab;
    [SerializeField] private Transform _parent;

    public void Run()
    {
        var sw = new Stopwatch();
        var random = new System.Random();
        var iterations = 100000;
        
        var text = Instantiate(_textPrefab, _parent);
        text.text = "----Start New Test----";
        text.alignment = TextAnchor.MiddleCenter;

        sw.Start();
        for (int i = 0; i < iterations; ++i)
        {
            int index = random.Next(_firstNamesList.Count);
            string randomName = _firstNamesList[index];
            _firstNamesList.Contains(randomName);
        }

        sw.Stop();
        text = Instantiate(_textPrefab, _parent);
        text.text = $"Linear List time: {sw.ElapsedMilliseconds}";

        sw.Reset();
        sw.Start();
        for (int i = 0; i < iterations; ++i)
        {
            int index = random.Next(_firstNamesList.Count);
            string randomName = _firstNamesList[index];
            _firstNamesList.BinarySearch(randomName);
        }

        sw.Stop();
        text = Instantiate(_textPrefab, _parent);
        text.text = $"Binary List time: {sw.ElapsedMilliseconds}";

        sw.Reset();
        sw.Start();
        for (int i = 0; i < iterations; ++i)
        {
            int index = random.Next(_firstNamesList.Count);
            string randomName = _firstNamesList[index];
            _firstNamesHashSet.Contains(randomName);
        }

        sw.Stop();
        text = Instantiate(_textPrefab, _parent);
        text.text = $"Hash time: {sw.ElapsedMilliseconds}";
    }
